export const NotationSymbol = ({
  type,
  duration,
  size = 16,
}: {
  type: 'note' | 'rest';
  duration: 'whole' | 'half' | 'quarter' | 'eighth' | 'sixteenth';
  size?: number;
}) => {
  const components = {
    note: {
      whole: WholeNote,
      half: HalfNote,
      quarter: QuarterNote,
      eighth: EighthNote,
      sixteenth: SixteenthNote,
    },
    rest: {
      whole: WholeRest,
      half: HalfRest,
      quarter: QuarterRest,
      eighth: EighthRest,
      sixteenth: SixteenthRest,
    },
  };

  const Symbol = components[type][duration];
  return <Symbol size={size} />;
};
